              <font size="2" face="Arial, Helvetica, sans-serif">What is SSL Protocol?</font></strong><font size="2" face="Arial, Helvetica, sans-serif"><br>

              Secure Sockets Layer (SSL) is the leading security protocol on the

              Internet. The protocol allows client/server applications to communicate

              in a way that is designed to prevent eavesdropping, tampering, or

              message forgery. SSL works by using a private key to encrypt data

              that is transferred over the SSL connection. Both Netscape Navigator

              and Internet Explorer support SSL, and many Web sites including

              www.fgb.com, use the protocol to obtain confidential user information.

              By convention, URLs that require an SSL connection start with https:

              instead of http:.</font></font></p>

