			  <p><strong><br>

			    Q) What is Smart Secure Desktop? </strong><br>

			    A) Smart Secure Desktop (SSD) is a new feature of FGB Online Banking that will

			    provide additional security for all your online banking transactions done with

			    First Bahrain Financial bank.<br>

			    <br>

			    <strong>Q) I already have antivirus, personal firewall &amp; other security

			    products installed in my computer. In that case do I require SSD ? </strong><br>

			    A) Yes. SSD will encrypt your Desktop and will prevent any unknown viruses or

			    trojans that are undetected by your antivirus or any other security products

			    from spying on the details of your transactions.<br>

			    <br>

			    <strong>Q) How does SSD work? </strong><br>

			    A) SSD installs itself as a small java client that will encrypt your desktop

			    thus making it more secure while you go about your online banking as you normally

			    do.<br>

			    <br>

			    <strong>Q) What are the benefits of using SSD and why should I use SSD ?</strong><br>

			    A) SSD makes Trojans, keyloggers and other man in the middle attacks (hacking

			    attacks) completely ineffective as it encrypts all your data while you are doing

			    online banking transactions with FGB.</p>

			  <p><br>

			    <strong>Q) Is not your current security sufficient enough to protect banking

			    transactions? </strong><br>

			    A) Currently FGB has advanced Security settings which are sufficient. Since

			    online threats are evolving considerably, we have to stay ahead of these threats.

			    That&#8217;s why FGB have introduced the extra bit of security.<br>

			    <br>

			    <strong>Q) What Operating Systems are supported by Smart Secure Desktop?</strong><br>

			    A) Windows ME, Windows 2000, Windows XP, Windows Vista and higher versions are

			    supported by the Smart Secure Desktop.<br>

			    <br>

			    <strong>Q) What Browsers are supported by Smart Secure Desktop?</strong><br>

			    A) &#8226; Internet Explorer 6 and 7 * <br>

			    &nbsp;&nbsp;&nbsp;&nbsp; &#8226; Mozilla Firefox(all versions) * Recommended

			    <br>

			    <br>

			    <strong>Q) Are there any other requirements to use Smart Secure Desktop?</strong><br>

			    A) The machine should have Java Virtual Machine installed for Smart Secure Desktop

			    to function. </p>

			  <p><strong>Q) Where can I get Java Virtual Machine?</strong><br>

			    A) Please download from http://www.java.com/en/download/windows_ie.jsp?locale=en&amp;host=www.java.com:80

			    <br>

			    <br>

			    <strong>Q) Does the Smart Secure Desktop install anything on my Machine?</strong><br>

			    A ) Yes. The Smart Secure Desktop Software will be installed for first time

			    users. This is only to make your future visits to the Smart Secure Desktop faster.

			    The software can be uninstalled. But the next visit to the Smart Secure Desktop

			    will reinstall the software.<br>

			    <br>

			    <strong>Q) Do I require administrator privileges in the machines to install

			    SSD software ? </strong><br>

			    A) No. Any normal user will be able to use this feature without having administrative

			    privileges on his machine.<br>

			    <br>

			    <strong>Q) What if I get an error saying that my computer&#8217;s security policy

			    is unsatisfactory?</strong><br>

			    A) It means that, your computer doesn&#8217;t satisfy minimal information security

			    requirements. You can still access the FGB website by Clicking &#8220;Continue&#8221;

			    and you will get the additional security of secure virtual desktop. However

			    we strongly recommend you to update your antivirus and other security settings

			  </p>

			  <p><br>

			    <strong>Q) Will it use my internet settings inside the virtual desktop? </strong><br>

			    A) Yes. Your internet will work as it does even without this software.<br>

			    <br>

			    <strong>Q) Will I be able to use my applications inside the Smart Secure Desktop?</strong><br>

			    A) All applications will function normally inside the Smart Secure Desktop as

			    they do on your normal desktop. The Smart Secure Desktop only prevents malicious

			    applications from functioning inside.<br>

			    <br>

			    <strong>Q) Will I be able to save files from my Smart Secure Desktop to my actual

			    desktop?</strong><br>

			    A) No. You can temporarily save your files inside the Smart Secure Desktop.

			    Once you Log Out of the Smart Secure Desktop, all files created inside the Smart

			    Secure Desktop will be permanently erased. If you want to save these files,

			    then they have to be saved on a USB Storage device.<br>

			    <br>

			    <strong>Q) Will I be able to print from inside the Smart Secure Desktop?</strong><br>

			    A) Yes . All printing functionality will work as usual from inside the Smart

			    Secure Desktop.<br>

			    <br>

			    <strong>Q) Will this slowdown my internet and internet banking transactions

			    </strong><br>

			    A) No. Your internet and internet banking transactions will work exactly as

			    it was working previously.<br>

			    <br>

			    <strong>Q)What should I do once I have logged out of FGB Internet Banking?</strong><br>

			    A) Once you have logged out of FGB Internet Banking, right click on the Smart

			    Secure Desktop Icon in your taskbar and click on exit Desktop.<br>

			    <br>

			    <strong>Q) How will I come out of Smart Secure Desktop? </strong><br>

			    A) In the right hand corner of your desktop, you will find a yellow lock. Kindly

			    click on the lock and you will get Exit &amp; Switch Desktop Options. If you

			    want to close the Smart Secure Desktop click on &#8220;Exit&#8221; otherwise

			    &#8220; Switch Desktop&#8221; to go back to your original window </p>

